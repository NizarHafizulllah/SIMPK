Selamat datang di SIMPK Kab. Sumbawa Barat</h3>
<hr>
Aplikasi SIMPK (Sistem Informasi Manajemen Pengendalian Kemiskinan) Kab. Sumbawa Barat merupakan aplikasi yang dikelola oleh Pemerintah Kabupaten Sumbawa Barat yang dimana berfunsi sebagai media publikasi data masyarakat miskin dikabupaten Sumbawa barat serta perbandingannya dengan Kabupaten-kabupaten lain yang berada di Provinsi Nusa Tenggara Barat.
<br>
<br>
Aplikasi SIMPK ini dibangun pada tahun 2016. dan terdiri dari beberapa menu yaitu <b> Pivot</b>, <b> Profil Daerah</b>, <b>Profil Program</b>, <b>Datamart</b>, <b>Grafik</b>, <b>Foto Kegiatan </b> 
